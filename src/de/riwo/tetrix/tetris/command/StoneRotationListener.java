package de.riwo.tetrix.tetris.command;

import de.riwo.tetrix.tetris.stone.Stone;

/**
 * Can be used to let other commands know about the changed
 * allocation of the stone.
 * @author richard
 *
 */
public interface StoneRotationListener {

	/**
	 * Called whenever the given {@link Stone} has been rotated
	 * by a {@link RotateCommand}.
	 * @param stone {@link Stone} that has been rotated.
	 */
	public void onStoneRotated(Stone stone);
	
}